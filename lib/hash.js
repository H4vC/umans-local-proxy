'use strict';

// FNV-1a 32-bit. Returns hex string. ~1ns/byte, no allocation beyond output.
function fnv1a(str) {
  let h = 0x811c9dc5;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return (h >>> 0).toString(16).padStart(8, '0');
}

module.exports = { fnv1a };
